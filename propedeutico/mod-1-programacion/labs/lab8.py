# Inicializar una variable para almacenar la suma
suma = 0  # Se inicia la suma en 0

# Usar un bucle for para iterar sobre un rango de números del 1 al 10
for numero in range(1, 11):  # range(1, 11) genera números del 1 al 10
    suma += numero  # Sumar el número actual a la suma total

# Imprimir el resultado de la suma
print(f"La suma de los números del 1 al 10 es: {suma}")  # Muestra el resultado final