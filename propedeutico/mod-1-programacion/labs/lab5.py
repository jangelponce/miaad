# Solicitar al usuario que ingrese un número
numero = float(input("Ingrese un número: "))  # Se convierte la entrada a tipo float

# Verificar si el número es positivo
if numero > 0:  # Comprueba si el número es mayor que 0
    print("El número es positivo.")  # Imprime si el número es positivo

# Verificar si el número es negativo
if numero < 0:  # Comprueba si el número es menor que 0
    print("El número es negativo.")  # Imprime si el número es negativo

# Verificar si el número es cero
if numero == 0:  # Comprueba si el número es igual a 0
    print("El número es cero.")  # Imprime si el número es cero