# Practica 2. Input y print en Python
# Descripción: Crea un programa que pida al usuario su nombre, edad, direccion, telefono,  y luego imprima un mensaje personalizado.

# Instrucciones:

# 1. Usa input() para recibir el nombre y la edad del usuario.
# 2. Imprime un mensaje de esta forma:
#    HOLA, MI NOMBRE ES:{NOMBREINGRESADO}, y TENGO {EDAD}AÑOS, Y VIVO EN {DIRECCION}, CON NUMERO DE TELEFONO{TELEFONO}
# 3. Envie su .py y una captura de pantalla con los resultados de su codigo

# 1. Captura de entradas
nombre = input("Ingresa tu nombre: ")
edad = input("Ingresa tu edad: ")
direccion = input("Ingresa tu direccion: ")
telefono = input("Ingresa tu telefono: ")

# 2. Impresión de mensaje personalizado
print("HOLA, MI NOMBRE ES:", nombre, "Y TENGO", edad, "AÑOS, Y VIVO EN", direccion, "CON NUMERO DE TELEFONO", telefono) 